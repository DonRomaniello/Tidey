// Record the initial time so the delta can be calculated later
const timeSubtract = new Date().getTime()
// This is how much to scale by
const unit = 10
const canvasSize = [500, 500]
// The amplitude indicators on the harmonics are called beads
const beadSize = 5
const speed = 10
let axesStrokeColor = 'rgba(0, 0, 0, 1)'
let featureFillColor = 'rgba(255, 255, 255, 1)'
let featureStrokeColor = 'rgba(0, 128, 255, 1)'
// How smooth the tide chart curves should be
const wavePrecision = 1

const numOfConstituents = 2
let constituents = harcon.HarmonicConstituents
constituents.sort((a, b) => b.amplitude - a.amplitude)
constituents = constituents.slice(0, numOfConstituents)

// declare a lot of variables that should not be redeclared each time
let canvas, ctx, height, width, xAxis, yAxis,
    x, y, nextXCenter, nextYCenter

const init = () => {
  // this may change, will revisit
  canvas = document.getElementById("canvas");

  canvas.width = 500;
  canvas.height = 500;

  ctx = canvas.getContext("2d");
  ctx.lineJoin = 'round';

  height = canvas.height;
  width = canvas.width;

  xAxis = Math.floor(height/2);
  yAxis = Math.floor(width/4);

  // Tracking the center of the current constituent is important

  ctx.save();
  window.requestAnimationFrame(draw);
}

const draw = () => {

  const time = new Date()

  nextXCenter = yAxis
  nextYCenter = xAxis

  ctx.clearRect(0, 0, width, height);

  // Draw the axes in their own path
  ctx.strokeStyle = axesStrokeColor;
  ctx.beginPath();
  drawAxes();

  // Set styles for animated graphics
  ctx.save();
  ctx.strokeStyle = featureStrokeColor;
  ctx.fillStyle = featureFillColor;
  ctx.lineWidth = 1;



  // Update the time and draw again
  draw.seconds = (time - timeSubtract) / (100000 / speed);
  draw.hourOffset = (time - timeSubtract) + (30000)
  draw.t = draw.seconds;
  runThroughConstituents(draw.t * Math.PI)
  ctx.restore();

  window.requestAnimationFrame(draw);
}

const runThroughConstituents = (time) => {

  constituents.forEach((constituent) => {

    const radius = Math.floor(constituent.amplitude * unit)
    ctx.beginPath()
    ctx.arc(nextXCenter, nextYCenter, radius, 20, 2*Math.PI, false);
    ctx.stroke();

    getLocationOnCircle(time, radius, constituent.phase_GMT)

    console.log(time)
  })

}

const getLocationOnCircle = (time, phase, radius) => {

  let nextCenters = getPhasedXY(time, phase, radius)
  nextXCenter = nextCenters[0]
  nextYCenter = nextCenters[1]
}

const getRadians = (angle) => {
  return (angle * (Math.PI / 180))
}

const getPhasedXY = (time, phase, radius) => {

  let phaseX = nextXCenter + radius * Math.sin((time + getRadians(phase)))

  let phaseY = nextYCenter + radius * Math.cos((time + getRadians(phase)))

  return [phaseX, phaseY]
}

const drawAxes = () => {
  // Draw X and Y axes
  ctx.strokeStyle = axesStrokeColor;
  ctx.moveTo(0, xAxis);
  ctx.lineTo(width, xAxis);
  ctx.moveTo(yAxis, 0);
  ctx.lineTo(yAxis, height);
  ctx.stroke();
}

init()
